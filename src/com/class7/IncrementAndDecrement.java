package com.class7;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		
	int z=10;
	z=z+1;//11
	
	z+=1;//12(add)
	System.out.println(z);//12
	
	z++;//++adds 1 to variable (12+1) and makes our value 13
	
	System.out.println(z);//13
	
	int x=20;
	
	x-=1;//19(subtraction)
	x--;//--subtracts 1 from variable (19-1) and makes our value 18
		
		System.out.println(x);//18
	
		
		
	}
}
